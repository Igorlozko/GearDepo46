import { Backdrop, CircularProgress } from '@mui/material';
import React from 'react';
import { useValue } from '../context/ContextProvider';

const Loading = () => {
  const {
    state: { loading }, // importing variable from the state default value is false
  } = useValue();
  return (
    <Backdrop open={loading} sx={{ zIndex: (theme) => theme.zIndex.modal + 1 }}> {/**xIndex means it gives it more priority, +1 theme of the modal to be on top of the modal */}
      <CircularProgress sx={{ color: 'white' }} /> {/**infinite circular progress animation */}
    </Backdrop>
  );
};

export default Loading;

